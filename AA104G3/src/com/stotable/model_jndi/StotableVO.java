package com.stotable.model_jndi;

public class StotableVO {
	private String tableno;
	private String stono;
	private Integer tablemin;
	private Integer tablemax;
	private Integer tablecount;
	private Integer tablestate;
	
	public String getTableno() {
		return tableno;
	}
	public void setTableno(String tableno) {
		this.tableno = tableno;
	}
	public String getStono() {
		return stono;
	}
	public void setStono(String stono) {
		this.stono = stono;
	}
	public Integer getTablemin() {
		return tablemin;
	}
	public void setTablemin(Integer tablemin) {
		this.tablemin = tablemin;
	}
	public Integer getTablemax() {
		return tablemax;
	}
	public void setTablemax(Integer tablemax) {
		this.tablemax = tablemax;
	}
	public Integer getTablecount() {
		return tablecount;
	}
	public void setTablecount(Integer tablecount) {
		this.tablecount = tablecount;
	}
	public Integer getTablestate() {
		return tablestate;
	}
	public void setTablestate(Integer tablestate) {
		this.tablestate = tablestate;
	}
	
}
